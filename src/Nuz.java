    static String bankhandelTitel(int art)
    {
        if (art < 5)
        {
            return "2:1-Handel mit " + rohstoffName(art, true);
        }
        if (art == 5)
        {
            return "3:1-Handel";
        }
        if (art == 6)
        {
            return "4:1-Handel";
        }
        if (art == 7)
        {
            return "Nimm 2 Rohstoffe";
        }
        throw new IllegalArgumentException("Unbekannte Art: " + art);
    }

